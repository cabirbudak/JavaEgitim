public class RelationalOperator {
    public static void main(String[] args) {
        // Değişken Tanımlaması:
        int number1 = 23;
        int number2 = 23;
        int number3 = 27;
        System.out.println("number1 > number2 = " + (number1 > number2));
        System.out.println("number2 > number3 = " + (number2 > number3));
        System.out.println("number2 == number3 = " + (number2 == number3));
    }
}